const divide = (num1, num2) => {
    const quotient = num1 / num2;
    return quotient;
}

const square = (num) => {
    const square = num * num;
    return square;
}

const add = (num1, num2) => {
    const sum = num1 + num2;
    return sum;
}






console.log(divide(10, 2));
console.log(square(8));
console.log(add(50, 36));




